CREATE TABLE IF NOT EXISTS users (
    user_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    email VARCHAR(255) NOT NULL,
    name VARCHAR(512) NOT NULL,
    CONSTRAINT UQ_USER_EMAIL UNIQUE (email)
);

CREATE TABLE IF NOT EXISTS items (
    item_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(255) NOT NULL,
    description VARCHAR,
    available BOOLEAN DEFAULT TRUE,
    owner_id BIGINT NOT NULL,
    request_id BIGINT,
    CONSTRAINT fk_item_owner FOREIGN KEY (owner_id) REFERENCES users(user_id)
);

CREATE TABLE IF NOT EXISTS bookings (
    booking_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    start_time TIMESTAMP WITHOUT TIME ZONE NOT NULL,
    end_time TIMESTAMP WITHOUT TIME ZONE NOT NULL,
    item_id BIGINT NOT NULL REFERENCES items (item_id) ON DELETE CASCADE,
    booker_id BIGINT NOT NULL REFERENCES users (user_id) ON DELETE CASCADE,
    status VARCHAR(20) NOT NULL
);

CREATE TABLE IF NOT EXISTS comments
(
    comment_id BIGINT GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    text TEXT NOT NULL,
    item_id BIGINT NOT NULL REFERENCES items (item_id) ON DELETE CASCADE,
    author_id BIGINT NOT NULL REFERENCES users (user_id) ON DELETE CASCADE,
    created TIMESTAMP WITHOUT TIME ZONE NOT NULL
);

CREATE TABLE IF NOT EXISTS item_requests
(
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    description  TEXT      NOT NULL,
    requestor_id BIGINT REFERENCES users (user_id),
    created TIMESTAMP WITHOUT TIME ZONE NOT NULL
);

ALTER TABLE items DROP CONSTRAINT fk_item_owner;
ALTER TABLE items ADD CONSTRAINT fk_item_owner
    FOREIGN KEY (owner_id) REFERENCES users(user_id) ON DELETE CASCADE;

ALTER TABLE item_requests DROP CONSTRAINT item_requests_requestor_id_fkey;
ALTER TABLE item_requests ADD CONSTRAINT item_requests_requestor_id_fkey
    FOREIGN KEY (requestor_id) REFERENCES users(user_id) ON DELETE CASCADE;
